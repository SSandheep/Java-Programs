public class DistanceOfLIght
{
    public static void main(String[] args)
    {
       int lightspeed;
       long days;
       long seconds;
       long Distance;
       lightspeed=18600;
       days=1000;
       seconds=days*24*60*60;
       Distance=lightspeed*seconds;
       System.out.println("In "+days+" days light will travel about "+Distance+" miles");
     }
    
}
